package com.mashup.shorts.domain.home.newscard.dto

import com.mashup.shorts.domain.news.News

data class DetailNewsCardResponse(
    var id: Long,
    var title: String,
    var thumbnailImageUrl: String,
    var newsLink: String,
    var press: String,
    var writtenDateTime: String,
    var type: String,
) {
    companion object {
        fun persistenceToResponseForm(newsBundle: List<News>): List<DetailNewsCardResponse> {
            return newsBundle.map {
                DetailNewsCardResponse(
                    id = it.id,
                    title = it.title,
                    thumbnailImageUrl = it.thumbnailImageUrl,
                    newsLink = it.newsLink,
                    press = it.press,
                    writtenDateTime = it.writtenDateTime,
                    type = it.type
                )
            }
        }
    }
}
